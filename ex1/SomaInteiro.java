public class SomaInteiros {
    public static void main(String[] args) {
        // Declara e inicializar os dois números inteiros
        int num1 = 5;
        int num2 = 10;
        
        // Soma os dois números
        int soma = num1 + num2;
        
        // Exibe o resultado
        System.out.println("A soma de " + num1 + " e " + num2 + " é: " + soma);
    }
}